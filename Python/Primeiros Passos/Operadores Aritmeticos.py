"""
+  -> Adição / Soma
-  -> Subtração / Diminui
*  -> Multiplicação / Realiza multiplicação
/  -> Divisão / Divide
// -> Divisão inteira realiza a divisão entre operandos
      e a parte decimal de ambos os operandos
%  -> Módulo / Retorna o resto da divisão
** -> Exponenciação / Eleva um número em uma potência
"""

numero1 = 10
numero2 = 3

adicao = numero1 + numero2
print("Resultado Adição:", adicao)
print("César" + " " + "Fonseca")

subtracao = numero1 - numero2
print("Resultado Subtração:", subtracao)

multiplicacao = numero1 * numero2
print("Resultado Multiplicação:", multiplicacao)
print(numero1 * "C")

divisao = numero1 / numero2
print("Resultado Divisão:", divisao)

divisaoInteira = numero1 // numero2
print("Resultado Divisão Inteira", divisaoInteira)

modulo = numero1 % numero2
print("Resultado Módulo:", modulo)

exponenciacao = numero1 ** numero2
print("Resultado Exponenciação:",exponenciacao)

